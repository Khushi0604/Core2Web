void main(){
  int ticket=1;

  if(ticket==1){
    print("Please pay 2000 rupees for Upper stand ticket.");
  }

  else if(ticket==2){
    print("Please pay 3000 rupees for Middle Stand ticket.");
  }

  else if(ticket==3){
