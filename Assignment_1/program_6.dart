//Program 6: A program that takes a number from 0 to 5 and prints its spelling, if number is greater than 5 print entered number is greater than 5

void main(){
  int a=4;


  if(a==0){
    print("Zero");
  }

  else if(a==1){
    print("One");

  }

  else if(a==2){
    print("Two");
  }

  else if(a==3){
    print("Three");
  }

  else if(a==4){
    print("Four");
  }

  else if(a==5){
    print("Five");
  }

  else{
    print("Number is greater than 5.");
  }

  //print("Please enter a new number.");
}
